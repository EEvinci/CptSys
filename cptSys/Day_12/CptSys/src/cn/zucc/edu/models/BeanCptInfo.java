package cn.zucc.edu.models;

public class BeanCptInfo {
	
	private String CptId;
	private String CptItemId;
	private String CptName;
	private String CptYear;
	private String HoldYear;
	private String HoldDay;
	private String RegiStart;
	private String RegiEnd;
	private String Contact;
	private String Web;
	
	

	public BeanCptInfo(String cptId, String cptItemId, String cptName, String cptYear, String holdYear, String holdDay,
			String regiStart, String regiEnd, String contact, String web) {
		super();
		CptId = cptId;
		CptItemId = cptItemId;
		CptName = cptName;
		CptYear = cptYear;
		HoldYear = holdYear;
		HoldDay = holdDay;
		RegiStart = regiStart;
		RegiEnd = regiEnd;
		Contact = contact;
		Web = web;
	}

	public BeanCptInfo() {
		super();
	}

	
	/*
	 * setter and getter
	 */

	public String getCptId() {
		return CptId;
	}

	public void setCptId(String cptId) {
		CptId = cptId;
	}

	public String getCptItemId() {
		return CptItemId;
	}

	public void setCptItemId(String cptItemId) {
		CptItemId = cptItemId;
	}

	public String getCptName() {
		return CptName;
	}

	public void setCptName(String cptName) {
		CptName = cptName;
	}

	public String getCptYear() {
		return CptYear;
	}

	public void setCptYear(String cptYear) {
		CptYear = cptYear;
	}

	public String getHoldYear() {
		return HoldYear;
	}

	public void setHoldYear(String holdYear) {
		HoldYear = holdYear;
	}

	public String getHoldDay() {
		return HoldDay;
	}

	public void setHoldDay(String holdDay) {
		HoldDay = holdDay;
	}

	public String getRegiStart() {
		return RegiStart;
	}

	public void setRegiStart(String regiStart) {
		RegiStart = regiStart;
	}

	public String getRegiEnd() {
		return RegiEnd;
	}

	public void setRegiEnd(String regiEnd) {
		RegiEnd = regiEnd;
	}

	public String getContact() {
		return Contact;
	}

	public void setContact(String contact) {
		Contact = contact;
	}

	public String getWeb() {
		return Web;
	}

	public void setWeb(String web) {
		Web = web;
	}
	


	


	
	
}
